class Syntax {
  void disp() {
    print("hello world");
  }
}

void main() {
  Syntax c = Syntax();
  c.disp();
}

/*The above example defines a class TestClass. The class has a method disp().
 The method prints the string “Hello World” on the terminal. The new keyword creates an object of the class. 
The object invokes the method disp(). */